package java0711;

public class Array1 {

	public static void main(String[] args) {
		int myArr[] = new int[5];
		
		for (int i=0; i<5; i++) {
			System.out.println(myArr[i]);
		}
		
		double myArr2[] = new double[5];
		
		for (int i=0; i<5; i++) {
			System.out.println(myArr2[i]);
		}
	
		boolean myArr3[] = new boolean[5];
		
		for (int i=0; i<5; i++) {
			System.out.println(myArr3[i]);
		}
	

	}

}
